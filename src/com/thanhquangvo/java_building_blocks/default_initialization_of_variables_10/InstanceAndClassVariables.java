package com.thanhquangvo.java_building_blocks.default_initialization_of_variables_10;

public class InstanceAndClassVariables {

    /* instance variable = class variable = field;
     * if not init them, them will have default value automation
     * boolean = false;
     * byte, short, int, long = 0;
     * float, double = 0.0
     * char = '\u0000'
     * all object reference = null
     */
    boolean defaultBoolean;
    int defaultInt;
    char defaultChar;
    Object defaultObject;
}
